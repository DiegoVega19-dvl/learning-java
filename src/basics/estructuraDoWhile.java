package basics;

import java.util.Scanner;

public class estructuraDoWhile {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		System.out.println("bienvenido al bucle do-while");

		int bucle = 5;
		do {
			System.out.println("esto es un do-while");

		} while (bucle != 5);
		{
			System.out.println("esto es un bucle");

		}

		sc.close();
	}

}
